#include "minishell.h"

t_bool	ft_isspace(char c)
{
	if (c == '\t' ||
		c == '\n' ||
		c == '\v' ||
		c == '\f' ||
		c == '\r' ||
		c == ' ')
		return (TRUE);
	return (FALSE);
}
