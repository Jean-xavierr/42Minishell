#include "global.h"

t_bool	l_isspace(char c)
{
	if (c == '\t' ||
		c == '\n' ||
		c == '\v' ||
		c == '\f' ||
		c == '\r' ||
		c == ' ')
		return (TRUE);
	return (FALSE);
}
