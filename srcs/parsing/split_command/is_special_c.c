#include "minishell.h"

int is_special_c(char *c)
{
	if (*c == '|' || *c == '<')
		return (*c == '|' || *c == '<');
	else if (ft_strncmp(c, ">>", 2) == 0)
		return ((ft_strncmp(c, ">>", 2) == 0));
	return (*c == '>');
}